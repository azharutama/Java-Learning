package pertemuan11;

public class LatihanWhile2 {
	public static void main(String[]args) {
		
		/*
		 * 
		 * buatlah program menggunakan while dimana pengulangn akan dilakukan sebanyak 10 kali dan tampilkan
		 * "angkatan 2022 jaya jaya jaya" sebanyak pengulangan tersebut
		 * 
		 */
		
		int loop= 1;
		while (loop <= 10) {
			System.out.println("angkatan 2022 jaya jaya jaya");
			loop++;
		}
	}
}
