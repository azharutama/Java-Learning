package pertemuan9;

public class PengulanganForDasar {
	public static void main(String[] args){
		/*
		 * pengulangan for adalah pengulangan terukur 
		 * karena sudah jekas pengulangan awal dan akhirnya
		 * terdapat 3 blok penting
		 * start 
		 * finish
		 * pengubah
		 * 
		 * format
		 * for (int i = 0(start); i < (finish); i++ (pengubah)) {
			  System.out.println(i);
			}
		 */
		System.out.println("pengulangan positif");
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
			
//				for positif
		}
		System.out.println("pengulangan negatif");
		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
			
//				for negatif
		}
	}
	

	
}
