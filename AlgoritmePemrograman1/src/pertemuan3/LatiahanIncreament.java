package pertemuan3;

public class LatiahanIncreament {
	public static void main(String []args) {
		int nilai= 10; 
		
		System.out.println("ini increament prefix==>" + (++nilai)); 
		System.out.println("ini increament pofix==>" + (nilai++)); 
		System.out.println("ini increament nilai akhir " + nilai); 
		
		System.out.println("ini increament prefix==>" + (--nilai)); 
		System.out.println("ini increament pofix==>" + (nilai--)); 
		System.out.println("ini increament nilai akhir " + nilai); 
		
	
	}

}
